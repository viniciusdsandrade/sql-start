/*
1- Descreva com suas palavras o que é um Banco de dados.
R: Um banco de dados é um conjunto de dados que são armazenados de forma organizada, com o objetivo de serem utilizados posteriormente.

2 - Descreva com suas palavras o que é um SGDB (Sistema Gerenciador de Banco de Dados)
R: Um SGDB é um sistema que gerencia o banco de dados, ou seja, ele é responsável por criar, alterar, excluir e consultar os dados do banco de dados.

3 - Descreva as principais diferenças entre o Banco de dados Relacional e o Banco de dados Não Relacional?
R: O banco de dados relacional é baseado no modelo relacional, que utiliza tabelas para armazenar os dados, enquanto o banco de dados não relacional é baseado em documentos, que são armazenados em coleções.

4 - O que é Linguagem de Manipulação de Dados (DML)?
R: A Linguagem de Manipulação de Dados (DML) é uma linguagem que permite a manipulação dos dados do banco de dados, ou seja, ela permite a inserção, alteração, exclusão e consulta dos dados.

5 - O que é Linguagem de Definição de Dados (DDL)?
R: A Linguagem de Definição de Dados (DDL) é uma linguagem que permite a definição dos dados do banco de dados, ou seja, ela permite a criação, alteração e exclusão de tabelas, índices, visões, etc.

6 - O que é uma Chave Primária e qual a função dela?
R: A Chave Primária é um campo que identifica unicamente cada registro de uma tabela, ou seja, ela é responsável por garantir a unicidade dos registros.

7 - O que é uma Chave Estrangeira e qual a função dela?
R: A Chave Estrangeira é um campo que referencia um campo de outra tabela, ou seja, ela é responsável por garantir a integridade referencial dos dados.

8 - O que é o Diagrama Entidade Relacionamento (DER) e qual a sua importância?
R: O Diagrama Entidade Relacionamento (DER) é um diagrama que representa as entidades e os relacionamentos entre elas, ou seja, ele é responsável por representar o modelo de dados do banco de dados.

9 - Quais são os 3 tipos de Relacionamento entre Tabelas no Modelo Relacional?
R: Os 3 tipos de relacionamento entre tabelas no modelo relacional são: um para um, um para muitos e muitos para muitos.

10 - Qual a importância do Banco de Dados para o desenvolvimento web?
R: O banco de dados é importante para o desenvolvimento web, pois ele é responsável por armazenar os dados da aplicação, ou seja, ele é responsável por armazenar os dados dos usuários, produtos, etc.
*/